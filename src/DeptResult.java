
public class DeptResult {
	public float aYearInterestedMoney;
	public float aMonthInterestedMoney;
	public float monthlyPayment;
	public float totalBorrow;
	
	public DeptResult() {}
	
	public float getAYearInterestedMoney() {
		return this.aYearInterestedMoney;
	}
	
	public float getAMonthInterestedMoney() {
		return this.aMonthInterestedMoney;
	}
	
	public float getMonthlyPayment() {
		return this.monthlyPayment;
	}
	
	public float getTotalBorrow() {
		return this.totalBorrow;
	}
	
	public void setAYearInterestedMoney(float aYearInterestedMoney) {
		this.aYearInterestedMoney = aYearInterestedMoney;
	}
	
	public void setAMonthInterestedMoney(float aMonthInterestedMoney) {
		this.aMonthInterestedMoney = aMonthInterestedMoney;
	}
	
	public void setMonthlyPayment(float monthlyPayment) {
		this.monthlyPayment = monthlyPayment;
	}
	
	public void setTotalBorrow(float totalBorrow) {
		this.totalBorrow = totalBorrow;
	}
	
}
